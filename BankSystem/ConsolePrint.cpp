#include "ConsolePrint.h"

/**
* Overload methods to print
* diffent parameters...
*/
void Print(string value) {
	cout << value;
}

void Print(int value) {
	cout << value;
}

void Print(float value) {
	cout << value;
}

void Println(string value) {
	cout << value << endl;
}

void Println(int value) {
	cout << value << endl;
}

void Println(float value) {
	cout << value << endl;
}